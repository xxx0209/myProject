package com.itgroup.coffee.domain;

public class Stemp {
    private int stempCount;
    private int rewordCount;

    public int getStempCount() {
        return stempCount;
    }

    public void setStempCount(int stempCount) {
        this.stempCount = stempCount;
    }

    public int getRewordCount() {
        return rewordCount;
    }

    public void setRewordCount(int rewordCount) {
        this.rewordCount = rewordCount;
    }
}
